// ── i18n helpers ──
let currentLang = 'es';

function detectLang() {
  const saved = loadPrefs().lang;
  if (saved && T[saved]) return saved;
  const nav = (navigator.language || '').slice(0, 2).toLowerCase();
  return T[nav] ? nav : 'es';
}

function t(key) {
  return (T[currentLang] && T[currentLang][key]) || T.es[key] || key;
}
